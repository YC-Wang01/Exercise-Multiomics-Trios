assign_clinical_group <- function(var_names) {
  dplyr::case_when(
    var_names %in% c("TRACP5a", "TRACP5b", "ALP", "cOC", "tOC", "cOC/tOC Ratio") ~ "Bone Metabolism",
    var_names %in% c("Fat Vi", "Fat Ve", "HFC", "A/L Ratio", "Fat Ip", "Fat Rp") ~ "Ectopic Adiposity",
    var_names %in% c("Fat(%)", "FM An", "FM Gy", "Fat Su", "BMI", "WC") ~ "Body Composition",
    var_names %in% c("Insulin", "Glucose", "ISI", "HOMA-IR", "CHOL", "LDL", "HDL", "TRIGLY", "NEFA") ~ "Glucose & Lipids",
    var_names %in% c("hsCRP", "WBC", "Leptin", "AST", "S-ALAT", "GGT", "Adiponectin", "SHBG", "Te", "E2", "IGF-1") ~ "Inflammation & Endocrine",
    var_names %in% c("NES", "HGS", "VJH", "EFS", "VO2max", "PAD", "PAF", "IPA", "REE", "AEE", "IEE", "Fat Mu") ~ "Fitness & Lifestyle",
    TRUE ~ "Other Traits"
  )
}

