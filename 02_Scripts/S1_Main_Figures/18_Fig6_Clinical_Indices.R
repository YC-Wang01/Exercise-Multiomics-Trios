        plot_df$Tissue_Rank <- ifelse(plot_df$Tissue == "Adipose", 2, 1)
        plot_df <- plot_df %>% dplyr::left_join(pathway_sig, by="CleanName") %>% dplyr::arrange(TotSig, Tissue_Rank)
        plot_df$Pathway_Tissue <- factor(paste0(plot_df$CleanName, " (", plot_df$Tissue, ")"), levels = unique(paste0(plot_df$CleanName, " (", plot_df$Tissue, ")")))
        
        # New \n Spacing to resolve ggh4x
        lvls <- c(paste("Pre", "Lean", h_label, sep="\n"),  paste("Pre", "Lean", l_label, sep="\n"),
                  paste("Pre", "Obese", h_label, sep="\n"), paste("Pre", "Obese", l_label, sep="\n"),
                  paste("Post3H", "Lean", h_label, sep="\n"),  paste("Post3H", "Lean", l_label, sep="\n"),
                  paste("Post3H", "Obese", h_label, sep="\n"), paste("Post3H", "Obese", l_label, sep="\n"))
        plot_df$X_Nested <- factor(paste(plot_df$Timepoint, plot_df$FatGroup, plot_df$OC_Level, sep="\n"), levels = lvls)
        
        p_bub <- ggplot(plot_df, aes(x = X_Nested, y = Pathway_Tissue)) +
          geom_point(aes(size = -log10(p.adjust), fill = NES), shape = 21, color = "black") +
          scale_fill_gradient2(low = "#3C5488", mid = "white", high = "#E64B35", midpoint = 0) +
          scale_size(range = c(3, 10)) +
          scale_y_discrete(position = "right") +
          theme_bw(base_size = 13) +
          labs(title = paste(index_name, "Spatiotemporal GSEA"), x = "", y = "") +
          theme(
            panel.grid = element_blank(), panel.border = element_rect(color="black", fill=NA, linewidth=1),
            axis.text.x = element_text(color="black", size=11, face="plain", lineheight=0.9),
            axis.text.y.right = element_text(color="black", hjust=0, size=13, face="plain"),
            axis.ticks = element_line(color="black"), plot.title = element_text(hjust = 0.5, face = "bold", size=16)
          )
        # Save GSEA mostly to Curated folder as it's curated by default
        ggsave(file.path(DIR_CURA, paste0("GSEA_Spatiotemporal_Bubble_", index_name, ".pdf")), p_bub, width = 10, height = 6.5)
      }
    }
  })
