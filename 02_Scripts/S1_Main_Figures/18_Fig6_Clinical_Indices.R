header_style <- createStyle(fontName = "Arial", fontSize = 11, fontColour = "white", fgFill = "#4F81BD", textDecoration = "bold")
sig_red      <- createStyle(fontColour = "#B2182B", textDecoration = "bold")
sig_blue     <- createStyle(fontColour = "#2166AC", textDecoration = "bold")

format_m1_excel <- function(data_list, out_path) {
  if(length(data_list) == 0) return()
  wb <- createWorkbook()
  for(idx in names(data_list)) {
    df <- data_list[[idx]] %>%
      dplyr::mutate(
        Lean_Sig = dplyr::case_when(Lean_P < 0.001 ~ "***", Lean_P < 0.01 ~ "**", Lean_P < 0.05 ~ "*", TRUE ~ "ns"),
        Obese_Sig = dplyr::case_when(Obese_P < 0.001 ~ "***", Obese_P < 0.01 ~ "**", Obese_P < 0.05 ~ "*", TRUE ~ "ns"),
        Sig_Tier = dplyr::case_when(Lean_P < 0.05 & Obese_P < 0.05 ~ 1, Lean_P < 0.05 | Obese_P < 0.05 ~ 2, TRUE ~ 3),
        Min_P = pmin(Lean_P, Obese_P, na.rm = TRUE)
      ) %>% dplyr::arrange(Sig_Tier, Min_P) %>% dplyr::select(-Sig_Tier, -Min_P) 
    
    addWorksheet(wb, idx); writeData(wb, idx, df)
    addStyle(wb, idx, style = header_style, rows = 1, cols = 1:ncol(df), gridExpand = TRUE)
    setColWidths(wb, idx, cols = 1:ncol(df), widths = "auto")
    
    col_l_cor <- match("Lean_Cor", names(df)); col_o_cor <- match("Obese_Cor", names(df))
    col_l_sig <- match("Lean_Sig", names(df)); col_o_sig <- match("Obese_Sig", names(df))
    
    conditionalFormatting(wb, idx, cols = col_l_cor, rows = 2:(nrow(df)+1), rule = ">0", style = sig_red)
    conditionalFormatting(wb, idx, cols = col_l_cor, rows = 2:(nrow(df)+1), rule = "<0", style = sig_blue)
    conditionalFormatting(wb, idx, cols = col_o_cor, rows = 2:(nrow(df)+1), rule = ">0", style = sig_red)
    conditionalFormatting(wb, idx, cols = col_o_cor, rows = 2:(nrow(df)+1), rule = "<0", style = sig_blue)
    conditionalFormatting(wb, idx, cols = col_l_sig, rows = 2:(nrow(df)+1), rule = "*", type = "contains", style = sig_red)
    conditionalFormatting(wb, idx, cols = col_o_sig, rows = 2:(nrow(df)+1), rule = "*", type = "contains", style = sig_red)
  }
  saveWorkbook(wb, out_path, overwrite = TRUE)
}

